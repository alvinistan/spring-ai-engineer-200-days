public class DataTypeDemo {
    public static void main(String[] args) {
        byte smallNumber = 100;
        short year = 2025;
        int marks = 95;
        long phoneNumber = 9876543210L;
        float productPrice = 499.99f;
        double bankBalance = 150000.75;
        char gender = 'M';
        boolean isJavaEasy = true;

        System.out.println("Byte value: " + smallNumber);
        System.out.println("Short value: " + year);
        System.out.println("Integer value: " + marks);
        System.out.println("Long value: " + phoneNumber);
        System.out.println("Float value: " + productPrice);
        System.out.println("Double value: " + bankBalance);
        System.out.println("Character value: " + gender);
        System.out.println("Boolean value: " + isJavaEasy);
    }
}